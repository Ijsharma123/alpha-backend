const mongoose = require("mongoose")
const scopeSchema = mongoose.Schema({
    job_id:{
        type:mongoose.Types.ObjectId,
        required:true
    },
    site_description:{
        type:String,
        required:false
    },
    scope_of_work:{
        type:String,
        required:false
    },
    purpose_of_serve:{
        type:String,
        required:false
    },
    introduction:{
        type:String,
        required:false
    },
    executive_summary:{
        type:String,
        required:false
    },
    added_by:{
        type:String,
        required:false
    },
    edit_by:{
        type:String,
        required:false
    },
    status:{
        type:String,
        required:true
    },
    added_at:{
        type:Date,
        default:Date.now()
    },
    update_at:{
        type:Date,
        default:Date.now()
    }
})
module.exports = mongoose.model("User_Scope", scopeSchema)